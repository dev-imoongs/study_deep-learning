# ### Deep Learning, 딥 러닝
# - 인공 신경망(Artificial Neural Network)의 층을 연속적으로 깊게 쌓아올려 데이터를 학습하는 방식을 의미한다.
# - 인간이 학습하고 기억하는 메커니즘을 모방한 기계학습이다.
# - 인간은 학습 시, 뇌에 있는 뉴런이 자극을 받아들여서 일정 자극 이상이 되면, 화학물질을 통해 다른 뉴런과 연결되며 해당 부분이 발달한다.
# - 자극이 약하거나 기준치를 넘지 못하면, 뉴런은 연결되지 않는다.
# - 입력한 데이터가 활성 함수(시그모이드 함수)에서 임계점을 넘게 되면 출력된다.
# - 초기 인공 신경망(Perceptron)에서 깊게 층을 쌓아 학습하는 딥 러닝(Input nodes layer, Hidden nodeds layer, Output nodes layer)으로 발전한다.
